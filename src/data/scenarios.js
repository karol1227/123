export function matchOfflineAnalysis(text) {
  const normalized = text.trim();
  if (!normalized) return null;

  for (const pattern of emotionPatterns) {
    if (pattern.keywords.some((kw) => normalized.includes(kw))) {
      return {
        literal: pattern.literal,
        emotional: pattern.emotional,
        emotions: pattern.emotions,
        needs: pattern.needs,
      };
    }
  }

  return { ...defaultFallback };
}

export function generateOfflineResponse(text, analysis) {
  const name = analysis.emotions[0] || "这些";
  return `我读到了你文字里的${name}感受。${analysis.emotional} 你不需要把所有事情都解释清楚——能被听见本身，就是一种温柔的开始。如果愿意，可以继续说说，我在这里。`;
}
